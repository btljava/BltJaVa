        txtquequan.setEnabled(false);
        txtsodienthoai.setEnabled(false);
        cbxNgaysinh.setEnabled(false);
        cbxtenlop.setEnabled(false);
        cbxthangsinh.setEnabled(false);
        cbxnamsinh.setEnabled(false);
