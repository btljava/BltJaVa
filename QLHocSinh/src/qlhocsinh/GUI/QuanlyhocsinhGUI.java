    private void uploaddata(List<HocSinhDTO> list) {
        LopDAO lop = new LopDAO();

        Object[][] data = new Object[list.size()][7];
        int i = 0;
        for (HocSinhDTO hs : list) {
            data[i][0] = hs.getTenhs();
            data[i][1] = hs.getMahs();
            data[i][2] = hs.getGioitinh();
            data[i][3] = hs.getNgaysinh();
            data[i][4] = hs.getQuequan();
            data[i][5] = hs.getSodienthoai();

            data[i][6] = hs.getTenlop();
            i++;
        }
        TableModel tableModel = new DefaultTableModel(data, colName);
        tablethongtinhocsinh.setModel(tableModel);
    }
