        rbtnNam.setEnabled(false);
        
        QuanLyHocSinhBLO qlhs = new QuanLyHocSinhBLO();
        HocSinhDTO hocsinh = new HocSinhDTO();
        HocSinhDAO hs = new HocSinhDAO();
        String mahs = txtmahs.getText();
        hocsinh.setMahs(mahs);
